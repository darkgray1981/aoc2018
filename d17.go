// Iterative approach
func drop(path []Pos, state *State) ([]Pos, bool) {

	b := state.Board

	p := path[len(path)-1]

	// No space to move
	if b[p] != 0 {
		return path, false
	}

	beenleft := false

	for {

		// Overflow or merge with existent flow
		if p.y >= state.MaxY || b[Pos{p.x, p.y + 1}] == '|' {
			b[p] = '|'
			return path, true
		}

		// Keep falling
		if b[Pos{p.x, p.y + 1}] == 0 {
			path = append(path, p)
			p = Pos{p.x, p.y + 1}
			beenleft = false
			continue
		}

		// Try flowing left
		if !beenleft && b[Pos{p.x - 1, p.y}] == 0 {
			p = Pos{p.x - 1, p.y}
			continue
		} else {
			beenleft = true
		}

		// Try flowing right
		if b[Pos{p.x + 1, p.y}] == 0 {
			p = Pos{p.x + 1, p.y}
			continue
		}

		// Try merging with flow
		c := byte('~')
		if b[Pos{p.x + 1, p.y}] == '|' {
			c = '|'
		}

		for b[Pos{p.x - 1, p.y}] == 0 {
			p = Pos{p.x - 1, p.y}
		}

		if b[Pos{p.x - 1, p.y}] == '|' {
			c = '|'
		}

		// Settle as water to the far side
		for b[p] == 0 {
			b[p] = c
			p = Pos{p.x + 1, p.y}
		}

		break
	}

	return path, true
}

// DFS recursive approach
func drip(p Pos, from Direction, state *State) bool {

	b := state.Board

	// Special case for undetermined left sides
	if b[p] == '?' {
		b[p] = b[Pos{p.x + 1, p.y}]
		return drip(Pos{p.x - 1, p.y}, RIGHT, state)
	}

	// Falling off the board or already handled
	if p.y > state.MaxY || b[p] == '|' {
		return true
	}

	// This tile is blocked
	if b[p] == '#' || b[p] == '~' {
		return false
	}

	// Free flowing below
	if drip(Pos{p.x, p.y + 1}, ABOVE, state) {
		b[p] = '|'
		return true
	}

	// Try flowing left
	if from != LEFT {
		if drip(Pos{p.x - 1, p.y}, RIGHT, state) {
			b[p] = '|'

			// Flow right if needed
			if from != RIGHT {
				// Pretend it's from above, so it notices flowing left
				drip(Pos{p.x + 1, p.y}, ABOVE, state)
			}
			return true
		}
	}

	// Left is blocked, try right
	if from != RIGHT {
		if drip(Pos{p.x + 1, p.y}, LEFT, state) {
			b[p] = '|'

			// Fill out to the left with the same type if needed
			if from != LEFT {
				drip(Pos{p.x - 1, p.y}, RIGHT, state)
			}
			return true
		}
	}

	// Need to check right side before we can tell
	if from == RIGHT {
		b[p] = '?'
		return false
	}

	// All sides blocked, turn into water
	b[p] = '~'

	// Fill out to the left if needed
	if from != LEFT {
		drip(Pos{p.x - 1, p.y}, RIGHT, state)
	}

	return false
}

func load(input string) *State {
	board := map[Pos]byte{Pos{500, 0}: '+'}

	minX, minY := 500, 999999999
	maxX, maxY := 500, -999999999

	for _, s := range strings.Split(input, "\n") {
		var a, b rune
		var i, j, k int

		scanned, err := fmt.Sscanf(s, "%c=%d, %c=%d..%d", &a, &i, &b, &j, &k)
		if err != nil || scanned != 5 {
			panic("Couldn't scan input! " + s)
		}

		if a == 'x' && b == 'y' {
			minX = min(minX, i)
			maxX = max(maxX, i)
			minY = min(minY, j)
			maxY = max(maxY, k)

			for p := j; p <= k; p++ {
				board[Pos{i, p}] = '#'
			}
		} else if a == 'y' && b == 'x' {
			minY = min(minY, i)
			maxY = max(maxY, i)
			minX = min(minX, j)
			maxX = max(maxX, k)

			for p := j; p <= k; p++ {
				board[Pos{p, i}] = '#'
			}
		} else {
			panic("Wrong axis!")
		}
	}

	state := &State{
		Board: board,
		MinX:  minX,
		MinY:  minY,
		MaxX:  maxX,
		MaxY:  maxY,
	}

	return state
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}

func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}

